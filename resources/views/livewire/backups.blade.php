    //
    public $backups = [];
    public $name = '';
    public $description = '';
    public $host = '';
    public $port = '';
    public $database = '';
    public $username = '';
    public $password = '';

    public function mount()
    {
        $this->backups = Backup::all();
    }
    public function store(){
        $backup = new Backup();
        $backup->user_id = auth()->user()->id;
        $backup->name = $this->name;
        $backup->description = $this->description;
        $backup->host = $this->host;
        $backup->port = $this->port;
        $backup->database = $this->database;
        $backup->username = $this->username;
        $backup->password = $this->password;
        $backup->save();
        $this->backups = Backup::all();
    }
    public function delete($backupId){
        $backup = Backup::find($backupId);
        $backup->delete();
        $this->backups = Backup::all();
    }
}; ?>

